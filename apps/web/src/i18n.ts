type TranslationNode = Record<string, unknown>;

const isObject = (value: unknown): value is TranslationNode => {
    return value !== null && typeof value === 'object' && !Array.isArray(value);
};

const deepMerge = (target: TranslationNode, source: TranslationNode): TranslationNode => {
    const next: TranslationNode = { ...target };
    for (const [key, sourceValue] of Object.entries(source)) {
        const targetValue = next[key];
        if (isObject(targetValue) && isObject(sourceValue)) {
            next[key] = deepMerge(targetValue, sourceValue);
        } else {
            next[key] = sourceValue;
        }
    }
    return next;
};

const mergeModuleTranslations = (base: TranslationNode, lang: string): TranslationNode => {
    const modules = getClientModules();
    let merged = { ...base } as TranslationNode;

    for (const moduleDef of modules) {
        const modTranslations = moduleDef.translations?.[lang];
        if (!isObject(modTranslations)) continue;
        merged = deepMerge(merged, modTranslations);
    }

    return merged;
};

const enResources = mergeModuleTranslations(enTranslations, 'en');
const esResources = mergeModuleTranslations(esTranslations, 'es');

const loadTranslationOverrides = async (locale: string) => {
    const normalized = String(locale || '').trim().toLowerCase();
    if (!SUPPORTED_LANGS.includes(normalized)) return;
    try {
        const res = await fetch(apiUrl(`/api/translations/overrides?locale=${encodeURIComponent(normalized)}`));
        if (!res.ok) return;
        const overrides = await res.json();
        if (!isObject(overrides)) return;
        i18n.addResourceBundle(normalized, 'translation', overrides.translation || {}, true, true);
    } catch {
        // Ignore override errors to keep the app boot resilient.
    }
};

i18n
    .use(initReactI18next)
    .init({
        resources: {
            en: enResources,
            es: esResources
        },
        lng: initialLanguage,
        fallbackLng: 'en',
        supportedLngs: SUPPORTED_LANGS,
        interpolation: {
            escapeValue: false // react already safes from xss
        }
    });

void loadTranslationOverrides(initialLanguage);
i18n.on('languageChanged', (lang) => {
    void loadTranslationOverrides(lang);
});

export default i18n;





